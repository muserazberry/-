"""자유 형식 제목(보도자료·민원 등) 중 입법과 무관한 신호를 걸러낸다.

국회 법률안처럼 제목이 곧 법령명인 출처와 달리, 보도자료·민원 제목은
인사·행사·외교 의전 등 조례와 무관한 내용이 많아 '제정/개정 추천' 오탐을
유발한다. 아래 비입법 표지어가 들어간 제목은 추천 대상에서 제외한다.

표지어는 '정책 제목에는 거의 안 쓰이는' 고정밀 단어만 담아 정상 신호를
잘못 걸러낼 위험을 줄였다 (예: '예방'·'방문'처럼 정책에도 흔한 말은 제외).
"""
import re

# 조례 제·개정과 무관한 비입법 신호 표지어
_NOISE = (
    # 인사·조직 동정
    "인사발령", "임명", "취임", "위촉", "내정", "부고", "별세", "동정",
    # 외교 의전·행사
    "정상회담", "정상회의", "회담", "접견", "순방", "친서", "의전",
    # 시상·기념·전시
    "시상식", "표창", "기념식", "개막식", "폐막식", "축제", "박람회",
    "전시회", "콘서트", "공연", "개봉", "관람", "화보",
    # 스포츠·연예
    "올림픽", "월드컵", "금메달", "은메달", "우승", "영화제", "음반", "앨범",
)
_NOISE_RE = re.compile("|".join(map(re.escape, _NOISE)))


def is_legislative(title: str) -> bool:
    """제목이 조례 제·개정 검토 대상이 될 수 있는지 (비입법 표지어가 없는지)."""
    return not _NOISE_RE.search(title or "")
